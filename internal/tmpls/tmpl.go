package tmpls

import (
	"fmt"
	"os"
	"path/filepath"

	"github.com/spf13/cobra"

	"github.com/yinweli/Sheeter/internal"
	"github.com/yinweli/Sheeter/internal/utils"
)

// Initialize 初始化
func Initialize(cmd *cobra.Command) error {
	flags := cmd.Flags()

	if flags.Changed(flagClean) {
		if clean, err := flags.GetBool(flagClean); err == nil && clean {
			_ = os.RemoveAll(internal.TmplPath)
		} // if
	} // if

	tmpls := []*Tmpl{
		JsonCsStruct,
		JsonCsReader,
		JsonCsDepot,
		JsonGoStruct,
		JsonGoReader,
		JsonGoDepot,
		ProtoSchema,
		ProtoCsReader,
		ProtoCsDepot,
		ProtoGoReader,
		ProtoGoDepot,
		ProtoCsBat,
		ProtoCsSh,
		ProtoGoBat,
		ProtoGoSh,
	}

	for _, itor := range tmpls {
		if err := itor.load(); err != nil {
			return fmt.Errorf("tmpl initialize failed: %w", err)
		} // if

		if err := itor.save(); err != nil {
			return fmt.Errorf("tmpl initialize failed: %w", err)
		} // if
	} // for

	return nil
}

// Tmpl 模板資料
type Tmpl struct {
	Name string // 模板檔名
	Data string // 模板字串
}

// load 讀取模板
func (this *Tmpl) load() error {
	path := this.path()

	if utils.FileExist(path) == false {
		return nil
	} // if

	data, err := os.ReadFile(path)

	if err != nil {
		return fmt.Errorf("%s: tmpl load failed: %w", this.Name, err)
	} // if

	this.Data = string(data)
	return nil
}

// save 儲存模板
func (this *Tmpl) save() error {
	if err := utils.WriteFile(this.path(), []byte(this.Data)); err != nil {
		return fmt.Errorf("%s: tmpl save failed: %w", this.Name, err)
	} // if

	return nil
}

// path 取得模板路徑
func (this *Tmpl) path() string {
	return filepath.Join(internal.TmplPath, this.Name)
}

