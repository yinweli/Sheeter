package builds

import (
	"os"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/suite"

	"github.com/yinweli/Sheeter/testdata"
)

func TestBuildJsonGo(t *testing.T) {
	suite.Run(t, new(SuiteBuildJsonGo))
}

type SuiteBuildJsonGo struct {
	suite.Suite
	workDir string
	code    []byte
	reader  []byte
}

func (this *SuiteBuildJsonGo) SetupSuite() {
	this.workDir = testdata.ChangeWorkDir()
	this.code = []byte(`
`) // TODO: 代碼內容等測試時來看看
	this.reader = []byte(`// generated by sheeter, DO NOT EDIT.

package sheeter

import "encoding/json"

type RealDataReader map[string]RealData

func (this *RealDataReader) JsonFileName() string {
	return "realData.json"
}

func (this *RealDataReader) FromJson(data []byte) error {
    return json.Unmarshal(data, this)
}
`)
}

func (this *SuiteBuildJsonGo) TearDownSuite() {
	_ = os.RemoveAll(pathSchema)
	_ = os.RemoveAll(pathJsonGo)
	testdata.RestoreWorkDir(this.workDir)
}

func (this *SuiteBuildJsonGo) content() *Content {
	content := &Content{
		Path:        testdata.RootPath,
		LineOfField: 1,
		LineOfLayer: 2,
		LineOfNote:  3,
		Excel:       testdata.ExcelNameReal,
		Sheet:       testdata.SheetName,
	}
	return content
}

func (this *SuiteBuildJsonGo) TestWriteJsonGo() {
	content := this.content()
	content.excel = testdata.GetTestExcel(testdata.ExcelNameReal)
	assert.Nil(this.T(), buildLayout(content))
	assert.Nil(this.T(), writeSchema(content))
	assert.Nil(this.T(), writeJsonGo(content))
	testdata.CompareFile(this.T(), content.JsonGoFilePath(), this.code)
	content.close()

	content = this.content()
	content.Excel = testdata.UnknownStr
	content.excel = testdata.GetTestExcel(testdata.ExcelNameReal)
	assert.Nil(this.T(), buildLayout(content))
	assert.Nil(this.T(), writeSchema(content))
	assert.NotNil(this.T(), writeJsonGo(content))
	content.close()

	content = this.content()
	content.Sheet = testdata.UnknownStr
	content.excel = testdata.GetTestExcel(testdata.ExcelNameReal)
	assert.Nil(this.T(), buildLayout(content))
	assert.Nil(this.T(), writeSchema(content))
	assert.NotNil(this.T(), writeJsonGo(content))
	content.close()
}

